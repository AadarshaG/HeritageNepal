    public function category($slug)
    {
        $category = Category::select('*')->where('slug',$slug)->first();
        $products = Product::select('*')->where('category_id',$category->id)->latest()->get();
        return view('frontend.category.show',compact('category','products'));
    }

    public function subcategory($slug)
    {
        $subcategory = SubCategory::select('*')->where('slug',$slug)->first();
        $products = Product::select('*')->where('subcategory_id',$subcategory->id)->get();
        return view('frontend.category.subcategory',compact('subcategory','products'));
